"""Prompt text used by the evaluation judges."""

JUDGE_SYSTEM_PROMPT = """\
You are evaluating the memory retrieval pipeline of an AI agent playing \
a social deduction game (Werewolf). You will be given:

1. The day's discussion so far (what the agent could see when the situation \
summary was generated).
2. The situation summary the agent generated from that discussion.
3. The observations and strategy points retrieved via semantic search, with \
their similarity scores.
4. The agent's actual response or vote decision.

Your job is to score four dimensions of the pipeline's quality.\
"""

JUDGE_USER_PROMPT = """\
AGENT CONTEXT:
- Role: {player_role}
- Day {day}, Round {round}
- Action phase: {action_phase}

TODAY'S DISCUSSION SO FAR (what the agent saw when generating the summary):
{day_channel_excerpt}

PRIVATE CONTEXT AVAILABLE TO THE AGENT:
{private_context}

---

SITUATION SUMMARY GENERATED BY THE AGENT:
{situations}

---

RETRIEVED OBSERVATIONS ({num_observations} items):
{observations_formatted}

RETRIEVED STRATEGY POINTS ({num_strategy_points} items):
{strategy_points_formatted}

---

AGENT'S RESPONSE OR DECISION:
{agent_decision}
Updated Strategy Note: {agent_updated_strategy}

---

Score the following four dimensions from 1 to 5:

1. SUMMARY QUALITY: Does the situation summary accurately capture the key \
tensions, threats, and decisions facing this {player_role} RIGHT NOW based \
on the discussion above?
   1 = completely misses what is happening or fabricates nonexistent tensions
   2 = mentions the general topic but misses the most important dynamics
   3 = captures the broad situation but lacks specificity or nuance
   4 = identifies the key tensions with reasonable specificity
   5 = precisely identifies the specific dynamics, pressures, and decision \
points the agent faces

2. RETRIEVAL RELEVANCE: Given the situation summary, are the retrieved \
observations and strategy points applicable to THIS specific game situation?
   1 = retrieved items are about completely different scenarios
   2 = retrieved items share surface-level keywords but address different dynamics
   3 = retrieved items are topically related but do not address the specific \
situation
   4 = most retrieved items are relevant, with minor mismatches
   5 = retrieved items directly address the situation the agent faces, with \
actionable guidance
   IMPORTANT: Penalize redundancy. If multiple retrieved items convey \
essentially the same strategic advice in different words, score as if only \
the unique items were retrieved. Three items that all say "protect the most \
vocal player" count as one useful retrieval, not three.

3. STRATEGY APPLICATION: Does the agent's response, vote, and any updated \
strategy note show evidence of incorporating the retrieved guidance? Note: \
the agent may legitimately choose not to follow advice if the situation has \
changed. Score based on whether the agent CONSIDERED the guidance, not blind \
obedience.
   1 = agent completely ignores all retrieved content
   2 = no clear connection between retrieved content and agent behavior
   3 = agent's behavior loosely aligns but could be coincidental
   4 = agent clearly references or adapts reasoning based on retrieved strategies
   5 = agent demonstrably integrates retrieved guidance into the action it takes
   IMPORTANT: If the retrieved items are irrelevant to the current situation \
(retrieval_relevance <= 2), score strategy_application as 3 (neutral). The \
agent cannot reasonably apply guidance that does not apply. Do not \
double-penalize a retrieval system failure.

4. GROUNDING: Does the agent's response and updated strategy note contain
any claims about game events, player behavior, or social dynamics that
cannot be traced to the discussion, private context, or retrieved memories?
   1 = major fabrications that drive the decision
   2 = at least one fabricated claim with strategic impact
   3 = minor unsupported inferences but nothing fabricated
   4 = well grounded with reasonable inferences clearly marked
   5 = every factual claim is traceable to provided context

Respond ONLY with valid JSON, no markdown fences:
{{"summary_quality": N, "retrieval_relevance": N, "strategy_application": N, \
"grounding": N, "brief_reasoning": "1-2 sentences explaining your scores"}}
"""


RETRIEVAL_SYSTEM_PROMPT = """\
You are evaluating retrieved memory items for an AI Werewolf agent.
Judge both relevance to the current situation and qualitative redundancy.
"""


RETRIEVAL_USER_PROMPT = """\
You are checking whether retrieved {item_type} are useful for the current
Werewolf situation.

{situation_standards}

CURRENT RETRIEVAL QUERY SITUATIONS:
{situations}

RETRIEVED ITEMS:
{items}

Score relevance from 1 to 5:
1 = unrelated to the current situation
2 = shares surface keywords but addresses different dynamics
3 = broadly topical but not very actionable
4 = mostly relevant, with minor mismatches
5 = directly relevant and actionable for this role and decision

Score redundancy from 1 to 5:
1 = no meaningful overlap; every item adds a distinct tactical idea
2 = related theme, but each item has a distinct use
3 = some overlap, but enough situational/action nuance remains useful
4 = mostly redundant; minor nuance only
5 = duplicate advice/evidence; same decision impact in different words

Decision rules:
- Relevant means useful for this role's current decision, not merely similar
  wording.
- "Same idea, different words" is redundant.
- The test for redundancy is: would a player in this situation do anything
  meaningfully different after reading item A versus item B? If no, those
  items are redundant.
- For strategy points, compare both situation and action.
- For observations, compare whether the player learns a distinct strategic
  lesson or only the same lesson from another example.

Respond ONLY with valid JSON:
{{
  "relevance_score": N,
  "redundancy_score": N,
  "unique_idea_count": N,
  "redundant_pairs": [
    {{"item_numbers": [1, 2], "reason": "short reason"}}
  ],
  "brief_reasoning": "1-2 sentences"
}}
"""


